//Function_in_js

//Simple function ---

function greet(){
    console.log('Hello World')
}
greet();

// Parametrized functions ---
function greet(name){
    console.log('Hello '+name)
}
greet('John')

// Multiple parameters functions ---

function greet(name, lastName)
{
    console.log('Hello '+name+' '+lastName)
}
greet('John', 'Smith')
